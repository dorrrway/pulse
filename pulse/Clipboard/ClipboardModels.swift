nonisolated enum ClipboardMarkerClassifier {
    static let knownTypes = Set(ClipboardKnownMarker.allCases.map(\.rawValue))
    static let legacyTransientTypes: Set<String> = [
        "de.petermaurer.TransientPasteboardType",
        "com.typeit4me.clipping",
        "Pasteboard generator type",
    ]

    static func markers(in types: some Sequence<String>) -> [String] {
        let markers = types.filter(isMarker)
        return Array(Set(markers)).sorted()
    }

    static func isMarker(_ type: String) -> Bool {
        knownTypes.contains(type)
            || legacyTransientTypes.contains(type)
            || inferredMarkerType(type)
    }

    static func shouldMaskByDefault(_ type: String) -> Bool {
        if let marker = ClipboardKnownMarker(rawValue: type) {
            return marker.shouldMaskByDefault
        }

        let normalizedType = type.lowercased()
        return legacyTransientTypes.contains(type)
            || normalizedType.contains("concealed")
            || normalizedType.contains("transient")
            || normalizedType.contains("autogenerated")
            || normalizedType.contains("auto-generated")
    }

    private static func inferredMarkerType(_ type: String) -> Bool {
        let normalizedType = type.lowercased()
        return normalizedType.contains("concealed")
            || normalizedType.contains("transient")
            || normalizedType.contains("autogenerated")
            || normalizedType.contains("auto-generated")
            || isRemoteClipboard(type)
    }

    static func isRemoteClipboard(_ type: String) -> Bool {
        if ClipboardKnownMarker(rawValue: type) == .remoteClipboard {
            return true
        }

        let normalizedType = type.lowercased()
        return normalizedType.contains("remote") && normalizedType.contains("clipboard")
    }
}

nonisolated enum ClipboardPasteboardTypeClassifier {
    static let plainTextTypes: Set<String> = [
        "public.utf8-plain-text",
        "public.utf16-plain-text",
        "public.plain-text",
        "NSStringPboardType",
        "public.text",
    ]

    static let urlTypes: Set<String> = [
        "public.url",
        "NSURLPboardType",
    ]

    static let fileURLTypes: Set<String> = [
        "public.file-url",
        "NSFilenamesPboardType",
    ]

    static let htmlTypes: Set<String> = [
        "public.html",
        "text/html",
        "Apple HTML pasteboard type",
    ]

    static let richTextTypes: Set<String> = [
        "public.rtf",
        "public.rtfd",
        "NSRTFPboardType",
        "NeXT Rich Text Format v1.0 pasteboard type",
    ]

    static let imageTypes: Set<String> = [
        "public.png",
        "public.tiff",
        "public.jpeg",
        "public.heic",
        "public.image",
        "Apple PNG pasteboard type",
        "NeXT TIFF v4.0 pasteboard type",
    ]

    static func isText(_ type: String) -> Bool {
        plainTextTypes.contains(type)
    }

    static func isURL(_ type: String) -> Bool {
        urlTypes.contains(type)
    }

    static func isFileURL(_ type: String) -> Bool {
        fileURLTypes.contains(type)
    }

    static func isHTML(_ type: String) -> Bool {
        htmlTypes.contains(type)
    }

    static func isRichText(_ type: String) -> Bool {
        richTextTypes.contains(type)
    }

    static func isImage(_ type: String) -> Bool {
        imageTypes.contains(type)
    }

    static func isImageFilePath(_ path: String) -> Bool {
        let fileExtension = URL(fileURLWithPath: path).pathExtension
        guard
            !fileExtension.isEmpty,
            let type = UTType(filenameExtension: fileExtension)
        else {
            return false
        }

        return type.conforms(to: .image)
    }

    static func imageType(forFilePath path: String) -> String {
        let fileExtension = URL(fileURLWithPath: path).pathExtension
        guard
            !fileExtension.isEmpty,
            let type = UTType(filenameExtension: fileExtension),
            type.conforms(to: .image)
        else {
            return "public.png"
        }

        return type.identifier
    }
}
