import Foundation
import UniformTypeIdentifiers

nonisolated enum ClipboardContentKind: String, Codable, Sendable {
    case text
    case url
    case file
    case image
    case mixed
    case data
}

nonisolated enum ClipboardContentFilter: String, CaseIterable, Sendable {
    case all
    case text
    case image
    case url
    case file
}

nonisolated enum ClipboardKnownMarker: String, CaseIterable, Codable, Sendable {
    case concealed = "org.nspasteboard.ConcealedType"
    case transient = "org.nspasteboard.TransientType"
    case autoGenerated = "org.nspasteboard.AutoGeneratedType"
    case source = "org.nspasteboard.source"
    case remoteClipboard = "com.apple.is-remote-clipboard"

    var shouldMaskByDefault: Bool {
        switch self {
        case .concealed, .transient, .autoGenerated:
            true
        case .source, .remoteClipboard:
            false
        }
    }
}

nonisolated struct ClipboardApplicationSource: Codable, Equatable, Sendable {
    var name: String?
    var bundleIdentifier: String?
    var bundlePath: String?
    var rawValue: String?

    var displayName: String? {
        name ?? bundleIdentifier ?? rawValue
    }
}

nonisolated enum ClipboardPasteboardAccessBehavior: String, Codable, Sendable {
    case systemDefault
    case ask
    case alwaysAllow
    case alwaysDeny
    case unknown
}

nonisolated enum ClipboardHistoryAccessIssue: Equatable, Sendable {
    case readDenied(ClipboardPasteboardAccessBehavior)
    case readFailed(String)
    case pasteCommandDenied
}

nonisolated struct ClipboardHistoryRecordNotice: Equatable, Identifiable, Sendable {
    var id: UUID
    var entryID: UUID
    var changeCount: Int
    var kind: ClipboardContentKind

    init(id: UUID = UUID(), entry: ClipboardHistoryEntry) {
        self.id = id
        self.entryID = entry.id
        self.changeCount = entry.changeCount
        self.kind = entry.kind
    }
}

nonisolated struct ClipboardStoredRepresentation: Codable, Equatable, Sendable {
    var type: String
    var blobID: String
    var byteCount: Int
}

nonisolated struct ClipboardHistoryItem: Codable, Equatable, Identifiable, Sendable {
    var id: UUID
    var kind: ClipboardContentKind
    var displayText: String
    var searchableText: String
    var markerTypes: [String]
    var representations: [ClipboardStoredRepresentation]
    var imageBlobID: String?
    var imagePixelWidth: Double?
    var imagePixelHeight: Double?

    var shouldMaskByDefault: Bool {
        markerTypes.contains(where: ClipboardMarkerClassifier.shouldMaskByDefault)
    }

    var blobIDs: [String] {
        var ids = representations.map(\.blobID)
        if let imageBlobID, !ids.contains(imageBlobID) {
            ids.append(imageBlobID)
        }
        return ids
    }
}

nonisolated struct ClipboardHistoryEntry: Codable, Equatable, Identifiable, Sendable {
    var id: UUID
    var changeCount: Int
    var createdAt: Date
    var kind: ClipboardContentKind
    var displayText: String
    var searchableText: String
    var markerTypes: [String]
    var declaredSource: ClipboardApplicationSource?
    var inferredSource: ClipboardApplicationSource?
    var items: [ClipboardHistoryItem]
    var fingerprint: String

    var shouldMaskByDefault: Bool {
        markerTypes.contains(where: ClipboardMarkerClassifier.shouldMaskByDefault)
            || items.contains(where: \.shouldMaskByDefault)
    }

    var primarySource: ClipboardApplicationSource? {
        declaredSource ?? inferredSource
    }

    var blobIDs: [String] {
        Array(Set(items.flatMap(\.blobIDs))).sorted()
    }

    func matches(searchText query: String) -> Bool {
        let normalizedQuery = query.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !normalizedQuery.isEmpty else {
            return true
        }

        return searchableText.localizedCaseInsensitiveContains(normalizedQuery)
            || displayText.localizedCaseInsensitiveContains(normalizedQuery)
            || markerTypes.joined(separator: " ").localizedCaseInsensitiveContains(normalizedQuery)
            || (primarySource?.displayName?.localizedCaseInsensitiveContains(normalizedQuery) ?? false)
    }

    func matches(contentFilter filter: ClipboardContentFilter) -> Bool {
        switch filter {
        case .all:
            true
        case .text:
            contains(kind: .text)
        case .image:
            contains(kind: .image)
        case .url:
            contains(kind: .url)
        case .file:
            contains(kind: .file)
        }
    }

    private func contains(kind targetKind: ClipboardContentKind) -> Bool {
        kind == targetKind || items.contains { $0.kind == targetKind }
    }
}

