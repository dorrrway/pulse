        XCTAssertEqual(store.lastCopiedEntryID, entry.id)
        XCTAssertEqual(store.accessIssue, .pasteCommandDenied)
    }

    func testDuplicateExternalPayloadMovesExistingEntryToTopWithoutDuplicating() throws {
        let store = makeStore()
        store.record(textSnapshot(changeCount: 1, text: "first"))
        store.record(textSnapshot(changeCount: 2, text: "second"))
        let originalFirstEntryID = try XCTUnwrap(store.entries.last?.id)

        store.record(textSnapshot(changeCount: 3, text: "first"))

        XCTAssertEqual(store.entries.count, 2)
        XCTAssertEqual(store.entries.first?.id, originalFirstEntryID)
        XCTAssertEqual(store.entries.first?.displayText, "first")
        XCTAssertEqual(store.entries.first?.changeCount, 3)
        XCTAssertEqual(store.entries.last?.displayText, "second")
    }

    func testDuplicatePlainTextURLRefreshesLegacyTextClassification() throws {
        let store = makeStore()
        let url = "https://github.com/dorrrway/pulse"
        store.record(textSnapshot(changeCount: 1, text: url))
        var legacyEntry = try XCTUnwrap(store.entries.first)
        legacyEntry.kind = .text
        legacyEntry.items[0].kind = .text
        store.entries[0] = legacyEntry
        let originalID = legacyEntry.id

        store.record(textSnapshot(changeCount: 2, text: url))

        let refreshedEntry = try XCTUnwrap(store.entries.first)
        XCTAssertEqual(store.entries.count, 1)
        XCTAssertEqual(refreshedEntry.id, originalID)
        XCTAssertEqual(refreshedEntry.changeCount, 2)
        XCTAssertEqual(refreshedEntry.kind, .url)
        XCTAssertEqual(refreshedEntry.items.first?.kind, .url)
    }

