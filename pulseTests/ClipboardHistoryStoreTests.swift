    func testMarkerBackedSensitiveItemsAreStoredSearchableAndCopyable() throws {
        let client = FakeClipboardPasteboardClient()
        let store = makeStore(client: client)
        let snapshot = ClipboardPasteboardSnapshot(
            changeCount: 1,
            capturedAt: Date(),
            declaredSource: ClipboardApplicationSource(
                name: "Password App",
                bundleIdentifier: "com.example.passwords",
                bundlePath: nil,
                rawValue: "com.example.passwords"
            ),
            inferredSource: nil,
            items: [
                ClipboardCapturedItem(
                    markerTypes: [ClipboardKnownMarker.concealed.rawValue],
                    representations: [
                        ClipboardCapturedRepresentation(
                            type: ClipboardKnownMarker.concealed.rawValue,
                            data: Data()
                        ),
                        ClipboardCapturedRepresentation(
                            type: "public.utf8-plain-text",
                            data: Data("123456".utf8)
                        ),
                    ]
                ),
            ]
        )

        store.record(snapshot)

        XCTAssertEqual(store.entries.count, 1)
        let entry = try XCTUnwrap(store.entries.first)
        XCTAssertTrue(entry.shouldMaskByDefault)
        XCTAssertTrue(entry.markerTypes.contains(ClipboardKnownMarker.concealed.rawValue))
        XCTAssertTrue(entry.matches(searchText: "123456"))

        store.copy(entry)

        XCTAssertTrue(client.writtenRepresentations.map(\.type).contains(ClipboardKnownMarker.concealed.rawValue))
        XCTAssertTrue(client.writtenRepresentations.contains { representation in
            representation.type == "public.utf8-plain-text"
                && String(data: representation.data, encoding: .utf8) == "123456"
        })
    }

