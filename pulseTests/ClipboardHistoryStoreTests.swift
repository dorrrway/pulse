    private func makeStore(
        client: FakeClipboardPasteboardClient = FakeClipboardPasteboardClient(),
        pasteCommandPoster: FakeClipboardFocusedPasteCommandPoster = FakeClipboardFocusedPasteCommandPoster()
    ) -> ClipboardHistoryStore {
        let rootURL = URL(fileURLWithPath: NSTemporaryDirectory(), isDirectory: true)
            .appendingPathComponent("PulseClipboardStoreTests-\(UUID().uuidString)", isDirectory: true)
        return ClipboardHistoryStore(
            userDefaults: UserDefaults(suiteName: "PulseClipboardStoreTests-\(UUID().uuidString)") ?? .standard,
            client: client,
            persistence: ClipboardHistoryPersistence(rootURL: rootURL),
            pasteCommandPoster: pasteCommandPoster
        )
    }

    private func textSnapshot(
        changeCount: Int,
        text: String,
        capturedAt: Date? = nil
    ) -> ClipboardPasteboardSnapshot {
        ClipboardPasteboardSnapshot(
            changeCount: changeCount,
            capturedAt: capturedAt ?? Date().addingTimeInterval(TimeInterval(changeCount)),
            declaredSource: nil,
            inferredSource: nil,
            items: [
                ClipboardCapturedItem(
                    markerTypes: [],
                    representations: [
                        ClipboardCapturedRepresentation(
                            type: "public.utf8-plain-text",
                            data: Data(text.utf8)
                        ),
                    ]
                ),
            ]
        )
    }
