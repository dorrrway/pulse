@MainActor
private final class FakeClipboardFocusedPasteCommandPoster: ClipboardFocusedPasteCommandPosting {
    var result: ClipboardFocusedPasteCommandResult = .posted
    var postPasteCommandCallCount = 0

    func postPasteCommand() -> ClipboardFocusedPasteCommandResult {
        postPasteCommandCallCount += 1
        return result
    }
}

@MainActor
private final class FakeClipboardPasteboardClient: ClipboardPasteboardClient {
    struct WrittenRepresentation: Equatable {
        var type: String
        var data: Data
    }

    var changeCount = 0
    var accessBehavior: ClipboardPasteboardAccessBehavior = .alwaysAllow
    var snapshot = ClipboardPasteboardSnapshot(
        changeCount: 0,
        capturedAt: Date(timeIntervalSince1970: 0),
        declaredSource: nil,
        inferredSource: nil,
        items: []
    )
    var writtenRepresentations: [WrittenRepresentation] = []

    func readSnapshot(capturedAt: Date, inferredSource: ClipboardApplicationSource?) throws -> ClipboardPasteboardSnapshot {
        snapshot
    }

    func write(entry: ClipboardHistoryEntry, blobData: [String: Data]) throws {
        changeCount += 1
        writtenRepresentations = entry.items.flatMap { item in
            item.representations.compactMap { representation in
                guard let data = blobData[representation.blobID] else {
                    return nil
                }

                return WrittenRepresentation(type: representation.type, data: data)
            }
        }
    }
}
